    //metodo para mostrar lista de médicos
    public void tabela(){
       MedicoDAO pdao = new MedicoDAO();
       List<Medico> listaMedico = pdao.listarM();
       DefaultTableModel modeloTabela = (DefaultTableModel) jTabela_m.getModel();
       modeloTabela.setRowCount(0); // zera as linhas
       for(Medico m: listaMedico){
           modeloTabela.addRow(new Object[]{m.getId_medico(), m.getNome(), m.getTelefone(), m.getEspecialidade()});
       }
    }

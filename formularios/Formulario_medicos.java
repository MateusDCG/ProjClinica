//metodo para excluir
    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
    int op = jTabela_m.getSelectedRow();
    Medico m = new Medico();
    
        if(op >= 0 ){
            m.setId_medico(Integer.parseInt(jTabela_m.getValueAt(op, 0).toString()));
            m.setNome(jTabela_m.getValueAt(op, 1).toString());
            m.setTelefone(jTabela_m.getValueAt(op, 2).toString());
            m.setEspecialidade(jTabela_m.getValueAt(op, 3).toString());
            MedicoDAO mdao = new MedicoDAO();
            mdao.excluir(m);
            tabela();
        }else{
            JOptionPane.showMessageDialog(null, "Escolha médico que deseja excluir.");
    }
    
    }//GEN-LAST:event_jButton5ActionPerformed
